// 获取国家中文名
export function getCnName(enName) {
  return EN_TO_CN[enName] || enName
}

// 获取国家坐标
export function getCoords(cnName) {
  return WORLD_COORDS[cnName] || null
}

// 获取所有国家列表
export function getCountryList() {
  return Object.keys(WORLD_COORDS)
}

// 获取地图数据格式（用于散点图）
export function getMapData(countryMap, totalAmtMap) {
  const entries = Object.entries(countryMap)
  return entries.map(([name, qty]) => {
    const coords = WORLD_COORDS[name] || null
    if (!coords) return null
    return {
      name,
      value: [...coords, qty],
      amount: totalAmtMap?.[name] || 0,
    }
  }).filter(Boolean)
}

